fn main() -> io::Result<()> {
    let args: Vec<String> = env::args().collect();
    let filename = &args[1];

    let mut buffer = Vec::new();
    let mut f = File::open(filename)?;
    f.read_to_end(&mut buffer)?;

    let mut pc = 0;

    while &pc < &buffer.len() {
        pc += disassemble(&buffer[pc..])
    }

    Ok(())
}

